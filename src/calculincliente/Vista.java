    private void tglBtnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tglBtnConectarActionPerformed
        if (tglBtnConectar.isSelected()) {
            if (conectar()) {
                e = Estados.CONECTADO;
                cambiarEstado(e);
            } else {
                tglBtnConectar.setSelected(false);
            }
        } else {
            salida.print("fin\r\n");
            salida.flush();
            
            e = Estados.DESCONECTADO;
            cambiarEstado(e);
        }
    }//GEN-LAST:event_tglBtnConectarActionPerformed

