    private void cambiarEstado(Estados e) {
        switch (e) {
            case CONECTADO:
                txtDireccionIP.setEnabled(false);
                txtPuerto.setEnabled(false);
                txtOperando1.setEnabled(true);
                txtOperando2.setEnabled(true);
                cBoxOperando.setEnabled(true);                
                btnCalcular.setEnabled(true);
                break;
            case DESCONECTADO:
                txtDireccionIP.setEnabled(true);
                txtPuerto.setEnabled(true);
                txtOperando1.setEnabled(false);
                txtOperando2.setEnabled(false);
                txtResultado.setText("");
                cBoxOperando.setEnabled(false);
                btnCalcular.setEnabled(false);
                break;
            default:
                break;
        }
    }
    
    private boolean conectar() {
        Socket conexion;
        int puerto;
        try {
            
            puerto = Integer.valueOf(txtPuerto.getText());
            conexion = new Socket(txtDireccionIP.getText(), puerto);
            if (conexion.isConnected()) {
                System.out.println("Conectado");
                
                entrada = new Scanner(conexion.getInputStream());
                salida = new PrintWriter(conexion.getOutputStream());
                return true;
            } else {
                JOptionPane.showMessageDialog(null, "Error al conectarse al servidor");
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Número de puerto no válido");
        }
        return false;
    }
    
